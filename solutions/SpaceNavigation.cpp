map<char,pair<int,int>> moves = { {'R',{0,1}}/*R*/,{'D',{1,0}}/*D*/,{'L',{0,-1}}/*L*/,{'U',{-1,0}}/*U*/ };
string S;
int px,py;

bool solve() {
  int lx=0,rx=0,ly=0,ry=0;
  REP(i,SZ(S)) {
    if(S[i]=='R') rx+=1;
    if(S[i]=='D') ly-=1;
    if(S[i]=='L') lx-=1;
    if(S[i]=='U') ry+=1;
  }
  return lx<=px&&px<=rx&&ly<=py&&py<=ry;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  cout<<setprecision(12)<<fixed;

  int t; cin>>t;
  while(t--) {
    cin>>px>>py>>S;
    cout<<(solve()?"YES":"NO")<<endl;
  }

  return 0;
}
