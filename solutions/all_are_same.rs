fn gcd(a: i32, b: i32) -> i32 {
    if b == 0 {
        a
    } else {
        gcd(b, a % b)
    }
}

fn main() {
    setup_out!(put, puts);

    let t = readln!(usize);
    for _ in 0..t {
        let _n = readln!(usize);
        let mut a = readln!([i32]);
        a.sort();
        a.dedup();

        if a.len() == 1 {
            puts!("-1");
            continue;
        }

        let mut g = (a[0] - a[1]).abs();
        for j in 0..a.len() {
            for i in 0..j {
                // dbgln!((a[j] - a[i]).abs(), g);
                g = gcd(g, (a[j] - a[i]).abs());
            }
        }

        puts!("{}", g);
    }
}
