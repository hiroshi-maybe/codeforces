fn main() {
    let out = std::io::stdout();
    let mut out = BufWriter::new(out.lock());
    macro_rules! puts {($($format:tt)*) => (let _ = write!(out,$($format)*););}
    input! {
        t: usize,
        s: [chars; t],
    }

    for s in s {
        let mut cnt=0;
        for (i, c) in s.iter().enumerate() {
            if *c=='0'&&(i==0||s[i-1]=='1') {
                cnt+=1;
            }
        }
        let res=min(cnt,2);
        puts!("{}\n", res);
    }
}
