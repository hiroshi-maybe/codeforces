#[allow(dead_code)]
fn f_v1(i: i64, x: i64) -> i64 {
    if x == 0 {
        return 0;
    }
    let s = (0..31).filter(|&i| ((x >> i) & 1) == 1).max().unwrap();

    let y = (1 << s) - 1;
    if i > s {
        0
    } else if i == s {
        x - y
    } else {
        (y + 1) / 2 + f(i, x - (1 << s))
    }
}

fn f(i: i64, x: i64) -> i64 {
    let x = x + 1;
    let b_size = 1 << (i + 1);
    let b_cnt = x / b_size;
    let rem = max(0, x - b_cnt * b_size);
    //dbgln!(i, x, b_size, b_cnt, rem);
    b_cnt * (1 << i) + max(0, rem - (1 << i))
}

fn solve() -> i64 {
    let (l, r) = readln!(i64, i64);

    (0..31)
        .map(|i| {
            let c1 = f(i, r) - f(i, l - 1);
            //dbgln!(i, r - l + 1, c1, r - l + 1 - c1);
            r - l + 1 - c1
        })
        .min()
        .unwrap()
}

fn main() {
    setup_out!(put, puts);

    let t = readln!(usize);
    for _ in 0..t {
        let res = solve();
        puts!("{}", res);
    }
}

