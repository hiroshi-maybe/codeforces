const string yes="YES",no="NO";
int X,Y,Z,T1,T2,T3;

string solve() {
  LL s=abs(X-Y)*T1;
  LL e=abs(Z-X)*T2+abs(X-Y)*T2+3LL*T3;
  return !(s<e)?yes:no;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  cin>>X>>Y>>Z>>T1>>T2>>T3;
  cout<<solve()<<endl;
  
  return 0;
}
