fn solve() -> bool {
    let n = readln!(usize);
    let s = readln!([char]);
    let t = readln!([char]);

    let mut x = vec![];
    let mut y = vec![];
    for i in 0..n {
        if s[i] == 'c' {
            x.push((2, i));
        } else if s[i] == 'a' {
            x.push((0, i));
        }
        if t[i] == 'c' {
            y.push((2, i));
        } else if t[i] == 'a' {
            y.push((0, i));
        }
    }

    if x.len() != y.len() {
        return false;
    }

    x.iter().zip(y.iter()).all(|(&(t1, p1), &(t2, p2))| {
        if t1 != t2 {
            return false;
        }
        if t1 == 0 {
            return p1 <= p2;
        }
        return p1 >= p2;
    })
}

fn main() {
    setup_out!(put, puts);

    for _ in 0..readln!(usize) {
        let res = solve();
        puts!("{}", if res { "YES" } else { "NO" });
    }
}

