fn f(a: &Vec<u8>, b: &Vec<u8>, par: usize) -> usize {
    let inf = 1_000_000;
    let d = (0..a.len()).filter(|&i| a[i] != b[i]).collect_vec();
    let mut cnt1 = d.iter().filter(|&&i| a[i] == 1).count();
    let mut cnt0 = d.len() - cnt1;
    //dbgln!(a, b, d.len(), cnt0, cnt1);

    if par == 1 {
        std::mem::swap(&mut cnt1, &mut cnt0);
    }

    if d.len() % 2 == par && (0..=1).contains(&(cnt1 as i32 - cnt0 as i32)) {
        cnt1 + cnt0
    } else {
        inf
    }
}

fn solve() -> i64 {
    let _n = readln!(usize);
    let a = readln!([u8]).iter().map(|x| x - b'0').collect_vec();
    let b = readln!([u8]).iter().map(|x| x - b'0').collect_vec();

    let inf = 1_000_000;
    let mut res = inf;
    {
        res.setmin(f(&a, &b, 0));
    }
    {
        let a = a.iter().map(|x| 1 - *x).collect_vec();
        res.setmin(f(&a, &b, 1));
    }

    if res == inf {
        -1
    } else {
        res as i64
    }
}

fn main() {
    setup_out!(put, puts);

    let t = readln!(usize);
    for _ in 0..t {
        let res = solve();
        puts!("{}", res);
    }
}

