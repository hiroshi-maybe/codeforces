///
/// 9/11/2021
///
/// 23:16-23:34 AC
///
fn main() {
    let out = std::io::stdout();
    let mut out = BufWriter::new(out.lock());
    macro_rules! puts {($($format:tt)*) => (let _ = write!(out,$($format)*););}
    input! {
        n: i32,
        cd: [(i64, i64); n],
    }
    for (c, d) in cd {
        let res = if c % 2 != d % 2 {
            -1
        } else if c == 0 && d == 0 {
            0
        } else if c == d {
            1
        } else {
            2
        };

        puts!("{}\n", res);
    }
}
