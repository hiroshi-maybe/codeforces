fn main() {
    setup_out!(put, puts);

    let t = readln!(usize);
    for _ in 0..t {
        let (n, k) = readln!(i64, usize);
        let mut a = readln!([i64]);
        a.sort_by_key(|&a| Reverse(a));
        let mut cp = 0;
        let mut res = 0;
        for i in 0..k {
            if cp < a[i] {
                res += 1;
            }
            let dis = n - a[i];
            cp += dis;
        }

        puts!("{}", res);
    }
}
