fn main() {
    setup_out!(put, puts);

    let t = readln!(usize);
    for _ in 0..t {
        let (n, x) = readln!(usize, usize);
        let a = readln!([i64]);

        let mut aa = a.clone();
        aa.sort();

        let mut ok = true;
        for i in 0..n {
            if a[i] != aa[i] {
                ok &= ((i as i32 - x as i32) >= 0) | ((i + x) < n);
            }
        }

        puts!("{}", if ok { "YES" } else { "NO" });
    }
}
